    private void refrescarTablaCorredores()
    {
    
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.setColumnIdentifiers(new String[]{"Nombre","DNI","Fecha Nacimiento","Direccion","Telf. Contacto","Dorsal","Carrera","Marca","Equipo"});
        
        List<Corredor> listaCorredores = LogicaNegocio.getListaCorredores();
        
        for(Corredor corredor : listaCorredores)
        {
        
            dtm.addRow(corredor.toArrayString());
        
        }
        
        jTableCorredores.setModel(dtm);
    
    }
    
    private void refrescarTablaCarreras()
    {
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.setColumnIdentifiers(new String[]{"Nombre","Fecha","Lugar","NÂºParticpantes"});
        
         List<Carreras> listaCarreras = LogicaNegocio.getListaCarreras();
        
        for (Carreras carrera: listaCarreras)
            
            dtm.addRow(carrera.toArrayString());
        
        jTableCarreras.setModel(dtm);
    }
    
   
    
    
    
